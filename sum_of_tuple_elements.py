def sum_of_tuple_elements(tpl):
    # Use the built-in sum() function to calculate the sum
    return sum(tpl)

# Example usage
my_tuple = (10, 20, 30, 40, 50)
total = sum_of_tuple_elements(my_tuple)

print(f"The tuple is: {my_tuple}")
print(f"The sum of all elements in the tuple is: {total}")
